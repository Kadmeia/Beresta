parties = ["ООО 'Вектор'", "ЗАО 'Альфа'", "ИП Смирнов А.А.", "ПАО 'Сбербанк'", "Иванов И.И.", "Петров П.П.", "Сидоров С.С.", "Адвокатская палата"]

scenarios = []
for idx, line in enumerate(raw_list.strip().split('\n')):
    if not line.strip(): continue
    
    parts = line.split('(')
    doc_type = parts[0].strip()
    meta = parts[1].replace(')', '') if len(parts) > 1 else ""
    
    layout = random.choice(['standard', 'bottom', 'messy'])
    
    p1 = random.choice(parties)
    p2 = random.choice(parties)
    while p1 == p2:
        p2 = random.choice(parties)
        
    if layout == 'standard':
        text = f"{doc_type.upper()}\\n{meta}\\n\\nСтороны: {p1} и {p2}\\n\\n(Текст документа...)\\nПодписи сторон."
    elif layout == 'bottom':
        text = f"{doc_type.upper()}\\n\\nСтороны: {p1} и {p2}\\n\\n(Много текста документа...)\\n...\\n(Конец документа)\\n\\nВыдано: {meta}\\nПодписи: ___________"
    elif layout == 'messy':
        messy_type = doc_type.upper().replace('О', '0').replace('А', 'A')
        text = f"   {messy_type}   \\n \\n{p1} \\n \\n{p2}\\n \\n(Teкст)\\n  {meta}"
        
    scenarios.append({
        "name": f"{idx+1}. {doc_type}",
        "expected_date_num": meta,
        "text": text,
        "layout": layout
    })

with open('test_data_criminal.json', 'w', encoding='utf-8') as f:
    json.dump(scenarios, f, ensure_ascii=False, indent=2)
